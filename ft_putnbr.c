#include "libft.h"
#include <unistd.h>

void	ft_putnbr(int n)
{
	char	c;
	int		x;

	if (n < 0)
	{
		x = 1;
		n = -n;
	}
	if (n > 9)
	{
		if (x == 1)
			write(1, "-", 1);
		ft_putnbr(n / 10);
	}
	c = n % 10 + '0';
	write(1, &c, 1);
}
