#include "libft.h"

int	ft_strnequ(char const *s1, char const *s2, size_t n)
{
	if (!(s1) && !(s2))
		return (1);
	if (!(s1) || !(s2))
		return (0);
	if (ft_memcmp(s1, s2, n) == 0)
		return (1);
	return (0);
}
