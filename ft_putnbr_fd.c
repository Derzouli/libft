#include "libft.h"
#include <unistd.h>

void	ft_putnbr_fd(int n, int fd)
{
	char	c;
	int		x;

	if (n < 0)
	{
		x = 1;
		n = -n;
	}
	if (n > 9)
	{
		if (x == 1)
			write(fd, "-", 1);
		ft_putnbr_fd(n / 10, fd);
	}
	c = n % 10 + '0';
	write(fd, &c, 1);
}
